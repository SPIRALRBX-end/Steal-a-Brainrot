    print("üìù Nomes carregados:", #targetNames)
    print("üéØ Clique no bot√£o para ativar/desativar")
    -- Scan inicial ap√≥s 2 segundos
    coroutine.wrap(function()
    	wait(2)
    	if AUTO_ACTIVATE then
    		scanExistingPrompts()
    	end
    end)()
end
